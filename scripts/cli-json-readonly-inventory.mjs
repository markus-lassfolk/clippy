#!/usr/bin/env node
/**
 * Emit a Markdown table: root command name, has any --json option in file, uses checkReadOnly.
 * Run from repo root: node scripts/cli-json-readonly-inventory.mjs
 * See docs/CLI_SCRIPTING_APPENDIX.md
 */
import { readdirSync, readFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const commandsDir = join(root, 'src', 'commands');
const skipFiles = new Set([
  'office-docs-shared.ts',
  'office-docs-drive-mirror.ts',
  'viva-extra-subcommands.ts',
  'viva-tenant-subcommands.ts',
  'mail-graph.ts',
  'drafts-graph.ts',
  'create-event-graph.ts',
  'findtime-graph.ts',
  'update-event-graph.ts',
  'viva-extra-subcommands.ts',
  'viva-tenant-subcommands.ts'
]);
const files = readdirSync(commandsDir)
  .filter((f) => f.endsWith('.ts') && !f.endsWith('.test.ts') && !skipFiles.has(f))
  .sort();

function fileUsesJsonOption(filePath) {
  const content = readFileSync(filePath, 'utf8');
  return (
    /\.option\(\s*['"]--json['"]/.test(content) ||
    /\.option\(\s*['"][^'"]*--json/.test(content) ||
    /\.option\(\s*['"][^'"]*,\s*['"][^'"]*--json/.test(content)
  );
}
const officeSharedPath = join(commandsDir, 'office-docs-shared.ts');
const officeSharedHasJson = fileUsesJsonOption(officeSharedPath);
const officeMirrorPath = join(commandsDir, 'office-docs-drive-mirror.ts');
const officeMirrorHasReadOnly = readFileSync(officeMirrorPath, 'utf8').includes('checkReadOnly(');

const rows = [];
for (const f of files) {
  const fullPath = join(commandsDir, f);
  const content = readFileSync(fullPath, 'utf8');
  let hasJson = fileUsesJsonOption(fullPath);
  let hasReadOnly = content.includes('checkReadOnly(');
  const m = content.match(/export const \w+Command = new Command\(\s*['"]([^'"]+)['"]/);
  const cmdName = m ? m[1] : f.replace(/\.ts$/, '');
  if ((cmdName === 'word' || cmdName === 'powerpoint') && officeSharedHasJson) {
    hasJson = true;
  }
  if ((cmdName === 'word' || cmdName === 'powerpoint') && officeMirrorHasReadOnly) {
    hasReadOnly = true;
  }
  rows.push({ file: f, cmdName, hasJson: hasJson ? 'yes' : 'no', hasReadOnly: hasReadOnly ? 'yes' : 'no' });
}

const md = [
  '<!-- AUTO-GENERATED by scripts/cli-json-readonly-inventory.mjs — do not edit by hand -->',
  '',
  '| Root command (from `Command("name")`) | Source file | `--json` anywhere | `checkReadOnly` used |',
  '| --- | --- | --- | --- |',
  ...rows.map((r) => `| \`${r.cmdName}\` | \`src/commands/${r.file}\` | ${r.hasJson} | ${r.hasReadOnly} |`),
  '',
  "_`--json`_: heuristic match for `.option(..., '--json'` or similar in the command module (subcommands vary)._",
  '_`checkReadOnly`_: module calls the guard for at least one mutating path; see [CLI_REFERENCE.md](./CLI_REFERENCE.md) for the authoritative read-only action list._',
  ''
].join('\n');

process.stdout.write(md);
